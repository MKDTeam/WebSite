	<div id="Description">
		Land Cruiser нового поколения в 200-м кузове продолжает традиции легендарного семейства. Этот внедорожник премиум-класса увидел свет в 2007 году. По-прежнему оставаясь настоящим рамным автомобилем, он задает новый уровень технического совершенства, оставляя за собой право считаться флагманом среди внедорожников. Новому Land Cruiser достались двигатели и система полного привода нового поколения. Существенно увеличились габариты автомобиля: длина подросла на 60 мм, высота и ширина — на 15 и 30 соответственно. Более роскошным стал интерьер, а многие опции, ранее доступные только в топовых комплектациях, предлагаются в стандартном оснащении.
