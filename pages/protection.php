Защита картера - конструкция, предназначенная для предотвращения механических повреждений узлов и агрегатов, расположенных в низших точках автомобиля.<br>
Защита картера разрабатывается с учетом индивидуальных особенностей автомобиля. Её конструкция зависит от типа кузова, объема двигателя, типа КПП, комплектации и прочих параметров. <br>
Ассортимент Sheriff™ представлен преимущественно штампованными защитами и насчитывает более 2500 моделей из алюминия и стали.
