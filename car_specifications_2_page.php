	<div id="FullDescription">
		Блок цилиндров и головки у мотора целиком выполнены из алюминия. Два турбокомпрессора расположены в развале цилиндров. Силовой агрегат установлен за передней осью, а трансмиссия находится в задней части автомобиля. Мотор доступен в двух исполнениях: 462 л.с. (600 Нм) и 510 л.с. (650 Нм). В первом случае разгон до 100 км/ч займет у спорткара 4 секунды, а во втором — 3,8 секунды. Максимальная скорость — 304 и 310 км/ч. Обе версии двигателя работают в паре с семиступенчатым «роботом» с возможностью ручного переключения передач.Кроме того, Mercedes-AMG GT по умолчанию комплектуется самоблокирующимся дифференциалом, но модификация с 462-сильным мотором оснащается механическим узлом, а более мощная версия получила электронную блокировку. Автомобиль можно оборудовать спортивной подвеской AMG Ride Control с возможностью электронной настройки амортизаторов, а также пакетом AMG Dynamic Plus, включающим динамические опоры двигателя и трансмиссии.
