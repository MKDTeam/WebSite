	<div id="Description">
		Модель AMG GT будет продаваться именно под маркой Mercedes-AMG, а не Mercedes-Benz — разработкой машины в большей степени занималось спортивное ателье AMG. Производитель позиционирует заднеприводное купе в качестве спорткара на каждый день, который составит конкуренцию Porsche 911. В модельном ряду «Мерседеса» GT займет место снятого с производства SLS AMG.Кузов купе имеет 4546 мм в длину, 1939 мм в ширину и 1289 мм в длину. Новый спорткар получился короче SLS AMG на 92 мм и выше на 27 мм, но сохранил аналогичную ширину. Вес новинки колеблется от 1540 до 1570 кг в зависимости от модификации — в конструкции модели широко используется алюминий. Для сокращения массы машины инженеры отказались от дверей в стиле «крыло чайки» в пользу привычных петель. Распределение веса купе по осям составляет 53:47 в пользу задней части.Специально для Mercedes-AMG GT с нуля был разработан новый 4,0-литровый турбодвигатель V8, носящий обозначение M178 (Евро-6). 
